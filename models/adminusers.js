const mongoose = require('mongoose');
const adminSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const AdminUser = mongoose.model('AmdinUser', adminSchema);
module.exports = AdminUser;
